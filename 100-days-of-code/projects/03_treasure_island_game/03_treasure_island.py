choice1 = input("You arrive at a junction. Do you go left or right? [left,right] ").lower()
if (choice1 == "left"):
  choice2 = input("You are standing on a river bank. Do you swim or wait for a boat? [swim, wait] ").lower()
  if (choice2 == "wait"):
    choice3 = input("You are in the Matrix. Do you choose the red, blue or yellow pill? [red,blue,yellow] ").lower()
    if (choice3 == "yellow"):
      print("Winner! You found a chest full of gold and Jack Sparrow's hat.")
    elif (choice3 == "red"):
      print("You enter a room and are burned by fire. Game Over!")
    elif (choice3 == "blue"):
      print("You are in a forest. You are eaten by a tiger. Game Over!")
    else:
      print("Game Over!")
  else:
    print("You are attacked by crocs. Game Over!")
else:
  print("You fall into a drainage hole and die. Game Over!")
